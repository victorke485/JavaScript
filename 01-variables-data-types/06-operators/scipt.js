// Arithmetic Operators
console.log(8 + 2);
console.log(8 - 2);
console.log(8 * 2);
console.log(8 / 2);
console.log(8 % 2); // Modulus
console.log(8 ** 2); // Exponent

// Assignment Operators
let x;
x = 10;
x += 10;
x -= 10;
x *= 10;
x /= 10;
x %= 10;
x **= 10;

// Comparison Operators
let a = 12;
let b = '12';
console.log(a == b);
console.log(a === b);
console.log(a != b);
console.log(a !== b);
console.log(a >= b);
console.log(a <= b);
