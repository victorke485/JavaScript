// Ways to display a variable - var, let, const

let firstName = 'Victor';
let lastName = 'Mutua';
let age = 20;

console.log(`FirstName: ${firstName}, LastName: ${lastName}, Age: ${age}`);

// Re-assigning Variables
age = 22;
console.log(age);

// Declare Variables
let x;
console.log(x);

// Declare multiple values at once
let a, b, c;
const d = 10,
  e = 20,
  f = 30;
console.log(d, e, f);
